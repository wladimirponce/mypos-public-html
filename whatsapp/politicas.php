        </ul>

        <h2>2. Uso de la Información</h2>
        <p>La información recopilada se utiliza exclusivamente para los siguientes propósitos:</p>
        <ul>
            <li>Proveer el servicio de atención al cliente y soporte técnico.</li>
            <li>Procesar pedidos, consultas o solicitudes realizadas a través del canal.</li>
            <li>Enviar notificaciones y actualizaciones relevantes sobre nuestros servicios (previa autorización).</li>
            <li>Mejorar la calidad de nuestras respuestas y servicios automatizados.</li>
        </ul>

        <h2>3. Compartir Información y Terceros</h2>
        <p>No vendemos, alquilamos ni compartimos la información personal de nuestros usuarios con terceros para fines comerciales o de marketing. La información solo se comparte en las siguientes circunstancias:</p>
        <ul>
            <li>Con <strong>Meta Platforms, Inc.</strong>, como proveedor de la infraestructura subyacente de WhatsApp, sujeto a sus propias políticas de privacidad.</li>
