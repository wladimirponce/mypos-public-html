        <h2>5. Conservación de los Datos</h2>
        <p>Retendremos la información de los mensajes y contactos solo durante el tiempo que sea necesario para cumplir con los fines descritos en esta política, resolver disputas o según lo requiera la normativa contable o legal aplicable.</p>

